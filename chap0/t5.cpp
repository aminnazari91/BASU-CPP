#include <iostream>
using namespace std;

int main(){
    int a, b;
	a = 2; 
	b = 6;
	// arithmetic operators 

	cout<< (a+b) << endl;
	cout<< (a-b) << endl;
	cout<< (a*b) << endl;
	cout<< (a/b) << endl;
	cout<< (a%b) << endl;
	cout<< (a++) << endl;
	cout<< a << endl;
	cout<< ++b << endl;
    
    return 0;
}