#include <iostream>
using namespace std;

int main(){
    // comparison operators
    int a, b;
	a = 2; 
	b = 6;
    
    cout<< (a>=b) << endl;
    cout<< (a>b) << endl;
    cout<< (a==b) << endl;
    cout<< (a!=b) << endl;
    cout<< (a<b) << endl;
    cout<< (a<=b) << endl;
    
    return 0;
}